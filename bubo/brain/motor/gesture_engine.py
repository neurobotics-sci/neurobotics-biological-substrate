logger = logging.getLogger("GestureEngine")


class GesturePriority(Enum):
    IDLE      = 0
    EMOTIONAL = 1
    SPEECH    = 2
    SAFETY    = 3   # highest — overrides all


@dataclass
class ArmState:
    """Current arm occupancy state — fed from spinal_arms node."""
    left_grasping:  bool  = False
    right_grasping: bool  = False
    left_carrying:  bool  = False
    right_carrying: bool  = False
    near_human:     bool  = False   # from peripersonal space monitor
    balance_ok:     bool  = True


@dataclass
class GestureCommand:
    """A gesture to execute."""
    name:           str
    priority:       GesturePriority = GesturePriority.EMOTIONAL
    duration_s:     float = 1.0
    head_nod:       float = 0.0    # degrees forward/back
    head_shake:     float = 0.0    # degrees left/right oscillation
    head_tilt_l:    float = 0.0    # degrees tilt left
    head_tilt_r:    float = 0.0    # degrees tilt right
    head_raise:     float = 0.0    # look up/down
    torso_lean:     float = 0.0    # forward (+) / back (-)
    arm_l_raise:    float = 0.0    # left arm raise degrees
    arm_r_raise:    float = 0.0    # right arm raise
    arm_l_spread:   float = 0.0    # left arm out to side
    arm_r_spread:   float = 0.0    # right arm out to side
    wrist_l_rotate: float = 0.0    # expressive wrist
    wrist_r_rotate: float = 0.0


