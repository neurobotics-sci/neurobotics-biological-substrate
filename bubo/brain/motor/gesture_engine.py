"""
bubo/brain/motor/gesture_engine.py — Bubo v10000

Expressive Gesture Engine: Head, Torso, and Arm Gestures

════════════════════════════════════════════════════════════════════
THE NEUROSCIENCE OF GESTURE
════════════════════════════════════════════════════════════════════

Human gesture is not decoration. It is a primary communication
channel that carries approximately 55% of emotional meaning
(Mehrabian 1967 — the original source of the often-misquoted 55/38/7).

Gestures are generated by the premotor cortex (SMA/PM) and are:
  1. Co-produced with speech (beat gestures, iconic gestures)
  2. Driven by emotional state (affective gestures)
  3. Modulatory — they change the meaning of the accompanying speech

For Bubo, gestures serve THREE purposes:
  SOCIAL: communicate emotional state and engagement to humans
  COGNITIVE: embodied thinking — gesture helps Bubo think
  SAFETY: always secondary to functional arm tasks

════════════════════════════════════════════════════════════════════
GESTURE PRIORITY SYSTEM
════════════════════════════════════════════════════════════════════

PRIORITY 1 — SAFETY: Never gesture while:
  - Carrying an object (both arms occupied)
  - Grasping with any hand (active grasp)
  - Within 20cm of a human (peripersonal space, S1 report)
  - Balance is compromised (IMU roll/pitch > 5°)

PRIORITY 2 — TASK COMPLETION: If one arm is in use, only the
  FREE arm and head/torso gestures are permitted.

PRIORITY 3 — EMOTIONAL EXPRESSION: When fully free, both arms
  and full-body expression is available.

PRIORITY 4 — IDLE ANIMATION: Subtle micro-movements when no
  interaction is occurring (prevents the "dead robot" uncanny valley).

════════════════════════════════════════════════════════════════════
GESTURE VOCABULARY
════════════════════════════════════════════════════════════════════

HEAD gestures (highest expressiveness per unit movement):
  NOD:          agreement, acknowledgement, "yes"
  SHAKE:        disagreement, "no"
  TILT_LEFT:    curiosity, thinking, processing
  TILT_RIGHT:   listening, empathy
  BOW:          greeting, gratitude
  LOOK_DOWN:    shame, thinking deeply
  LOOK_UP:      recall, imagination, hope

TORSO gestures:
  LEAN_FORWARD: engagement, curiosity, excitement ("input!")
  LEAN_BACK:    surprise, uncertainty, taking stock
  UPRIGHT:      neutral, confident, ready
  SLIGHT_BOW:   greeting, respect

ARM gestures (when arms are free):
  OPEN_ARMS:    welcoming, inclusive, "come here"
  ARMS_OUT:     explaining/presenting something
  RAISE_ONE:    offering, pointing, "look at this"
  CROSSED:      thinking, self-contained (low arousal states)
  HANDS_UP:     surprise, "I don't know", uncertainty
  GESTURE_SPEECH: subtle wrist movements during speaking

COMBINED / SIGNATURE GESTURES:
  GREETING:     bow + open arms (meeting someone new, bond_level < 0.3)
  RECOGNITION:  slight lean forward + head tilt (seeing familiar person)
  DEEP_THOUGHT: tilt head + one hand near chin (processing hard question)
  DELIGHT:      lean forward + hands out + eye cover flutter
  LISTENING:    tilt right + lean slightly forward + stillness
"""

import time
